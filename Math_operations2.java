public class Math_operations2 {
    public static void main(String[] args) {
        double result1 = 157.70;
        result1 += 40.40;
        System.out.println("The result of the operation: 157.70 + 40.40 = "+result1);

        int result2 = 48000;
        result2 -= 40000;
        System.out.println("The result of the operation: 48000 - 40000 = "+result2);

        short result3 = 25;
        result3 *= 10;
        System.out.println("The result of the operation: 25 * 10 = "+result3);

        float result4 = 10.002f;
        result4 /= 2;
        System.out.println("The result of the operation: 10.002 / 2 = "+result4);

        int result5 = -192;
        result5 %= 100;
        System.out.println("The result of the operation: -192 % 100 = "+result5);
    }
}
