public class Math_operations1 {
    public static void main(String[] args) {
        double x = 45.30, y = 12.40;
        int z = 100;
        double result = x + y + z;
        System.out.println("The result of the operation: 45.30 + 12.40 + 100 = " + result);

        int num1 = 50, num2 = 342, num3 = 100;
        int result1 = num1 - num2 + num3;
        System.out.println("The result of the operation: 50 - 342 + 100 = " + result1);

        double num4 = 54.54, num5 = 53.53;
        double result2 = num4 - num5;
        System.out.println("The result of the operation: 54.54 - 53.53 = " + result2);

        int num6 = 50;
        double num7 = 75.50;
        double result3 = num6 * num7;
        System.out.println("The result of the operation: 50 * 75.50 = " + result3);

        int num8 = 40;
        long num9 = 80, num10 = 3;
        short num11 = 5;
        long result4 = num8 * num9 * num10 * num11;
        System.out.println("The result of the operation: 40 * 80 * 3 * 5 = " + result4);

        double num12 = 100, num13 = 20.5;
        double result5 = num12 / num13;
        System.out.println("The result of the operation: 100 / 20.5 = " + result5);

        float num14 = 1000.2f, num15 = 100;
        float result6 = num14 / num15;
        System.out.println("The result of the operation: 1000.2 / 100 = " + result6);

        int num16 = 115, num17 = 20;
        int result7 = num16 % num17;
        System.out.println("The result of the operation: 115 % 20 = " + result7);

        float num18 = 500.456f, num19 = 500;
        float result8 = num18 % num19;
        System.out.println("The result of the operation: 500.456 % 500 = " + result8);


    }
}
